#include "libft.h"

int	ft_isalnum(int chr)
{
	if ((chr >= 65 && chr <= 90) || (chr >= 65 && chr <= 122))
		return (1);
	else if (chr >= 48 && chr <= 57)
		return (1);
	else
		return (0);
}
