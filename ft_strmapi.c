#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(size_t, char))
{
	size_t			i;
	char			*str;

	str = ft_strdup(s);
	i = 0;
	while (str[i] != '\0')
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
